// добавление API попапа в глобалную видимость
window.popup = {
	open(id) {
		if (!id) return;

		let popup = document.querySelector(id);

		if (!popup) return;

		popupOpen(popup);
	},
	close(id) {
		if (!id) return;

		let popup = document.querySelector(id);

		if (!popup) return;

		popupClose(popup);
	}
}


const ticker = {
	seconds: 120,
	currentTick: 0,

	_tick() {
		let id = setInterval(() => {
			this.currentTick++

			if (this.currentTick >= this.seconds) {
				this.currentTick = 0;
				if (this.cl) {
					this.cl();
					clearInterval(id);
				};
			}
		}, 1000)
	},

	init(cl) {
		this.cl = cl;
		this._tick();
	},

	reset() {
		this.currentTick = 0;
	}
}

ticker.init(() => {window.popup.open('#dont-leave')});

document.addEventListener('keydown', (e) => {
	ticker.reset();
});

document.addEventListener('mousemove', (e) => {
	ticker.reset();
})

document.addEventListener('click', () => {
	ticker.reset();
})
document.addEventListener('scroll', () => {
	ticker.reset();
})